    public void setModel(String word, DefaultListModel lm, String selectedLang) {
        Font font = new Font("Iskoola pota", Font.PLAIN, 16);
        list_select.setFont(font);
        this.selectedLang = selectedLang;
        
        /*  When putting the same model which got from the dictionay page to the selection list,
            it removes the String from the dictionary result List aswell because its the same model. 
            Below for loop is for transforming it to a different named model object.         
        */
        DefaultListModel dlmForSelection = new DefaultListModel ();
        for (int i=0; i<lm.getSize(); i++) {
            String x = (String) lm.getElementAt(i);
            dlmForSelection.addElement(x);
        }
        
        list_select.setModel(dlmForSelection);
        this.word = word;
    }

