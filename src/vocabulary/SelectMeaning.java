    private void btn_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_saveActionPerformed
        DefaultListModel dml = (DefaultListModel) list_select.getModel();
        int selectedIndex = list_select.getSelectedIndex();
        
        //to pass to saveWord method
        String selectedMeaning = list_select.getSelectedValue().substring(4).trim();
        String otherMeanings = "";
        
        System.out.println(selectedMeaning);
        dispose();
        dml.removeElementAt(selectedIndex);
        
        for (int i=0; i< dml.size(); i++) {
            otherMeanings = otherMeanings+dml.get(i).toString().substring(4).trim();
            if (i!=dml.size()-1)
                otherMeanings = otherMeanings+" / ";    //Anthima ekata passe "/" watena eka nathara karanna. 
        }
        System.out.println(otherMeanings);
        
        SQLiteVocabulary handler = new SQLiteVocabulary();
        handler.insertWord(word, selectedMeaning, otherMeanings, this.selectedLang);
        JOptionPane.showMessageDialog(null, "Word added to your reminder list.");
    }//GEN-LAST:event_btn_saveActionPerformed

